// ======================================================================
// reports/help.js
// Модальное окно со справкой по использованию страницы «Шаблоны отчётов».
// Открывается из кнопки «?» в тулбаре каталога. Справка рассчитана на
// конечного пользователя — инженера, который готовит шаблоны для себя
// и своей команды.
//
// Содержит:
//   — что такое шаблон и чем он отличается от «содержимого»
//   — как создать, отредактировать, клонировать, удалить
//   — как подпрограммы (cable/, battery/, ...) используют эти шаблоны
//   — где шаблоны физически хранятся (браузер, per-user)
//   — как делать резервное копирование (импорт / экспорт JSON)
//
// Для разработчиков, которые встраивают модуль в свою подпрограмму,
// есть отдельный документ: shared/report/README.md.
// ======================================================================

export function openHelp() {
  const backdrop = document.createElement('div');
  backdrop.className = 'rpt-modal-backdrop';
  const modal = document.createElement('div');
  modal.className = 'rpt-modal';
  modal.style.maxWidth = '780px';
  modal.style.height = 'auto';
  modal.style.maxHeight = '90vh';
  modal.innerHTML = `
    <div class="rpt-modal__hdr">
      <div class="rpt-modal__title">Как пользоваться шаблонами отчётов</div>
      <div>
        <button class="primary" id="help-close">Закрыть</button>
      </div>
    </div>
    <div class="rpt-help-body">
      <section>
        <h3>Что такое шаблон</h3>
        <p>Шаблон отчёта — это <b>оформление</b> документа: формат листа, поля печати,
        логотип, колонтитулы (шапка и подвал), стили заголовков, основного текста,
        списков и таблиц. Шаблон <b>не хранит</b> данные расчёта.</p>
        <p>Данные подставляет подпрограмма, когда вы нажимаете «Экспорт отчёта»
        в «Расчёте кабельной линии», «Расчёте АКБ», «Конфигураторе щита» и т.д.
        Одну и ту же подпрограмму можно выводить в разных шаблонах — выбирая
        подходящий при экспорте.</p>
      </section>

      <section>
        <h3>Встроенные и пользовательские</h3>
        <p>В каталоге есть два типа шаблонов:</p>
        <ul>
          <li><b>Встроенные</b> — идут в комплекте, подходят для частых задач
          (инженерный отчёт, расчёт кабеля, АКБ, пояснительная записка, ведомость,
          карточка A5 и др.). Их нельзя удалить или переименовать, но можно
          <b>клонировать</b> — копия станет «моим» шаблоном, и его уже можно
          править.</li>
          <li><b>Мои</b> — шаблоны, которые вы создали или клонировали. Их можно
          редактировать, переименовывать, удалять и экспортировать.</li>
        </ul>
      </section>

      <section>
        <h3>Как создать новый шаблон</h3>
        <ol>
          <li>Нажмите <b>+ Новый</b> слева в тулбаре.</li>
          <li>Задайте название и (по желанию) короткое описание.</li>
          <li>Автоматически откроется окно «Настройка шаблона отчёта» — в нём
          вкладки <b>Страница / Поля / Колонтитулы / Логотип / Стили / Метаданные</b>.</li>
          <li>Настройте оформление и нажмите <b>Сохранить</b> — шаблон попадёт в
          ваш каталог.</li>
        </ol>
        <p>Если вам нужен шаблон, похожий на уже существующий — проще
        не создавать с нуля, а <b>клонировать</b> близкий встроенный шаблон и
        подправить оформление клона.</p>
      </section>

      <section>
        <h3>Превью и экспорт тестового PDF</h3>
        <p>Выбранный шаблон показывается справа в виде страниц с демо-данными —
        так вы сразу видите, как ляжет реальный отчёт. Кнопки <b>PDF</b> и <b>DOCX</b>
        в верхней панели формируют файл с этим же демо-контентом, чтобы проверить
        оформление в Word или Acrobat прежде чем использовать шаблон в подпрограмме.</p>
        <p>Демо-данные для встроенных шаблонов подобраны по теме шаблона: расчёт
        кабеля показывает таблицу с сечениями, АКБ — параметры разряда, ведомость —
        спецификацию материалов и т.д.</p>
      </section>

      <section>
        <h3>Как подпрограммы применяют шаблон</h3>
        <p>Когда вы в подпрограмме (например, «Расчёт кабельной линии») нажимаете
        «Экспорт отчёта», она:</p>
        <ol>
          <li>Читает список ваших шаблонов и предлагает выбрать подходящий.</li>
          <li>Берёт оформление из шаблона (страницу, поля, стили, колонтитулы).</li>
          <li>Вставляет <b>свои</b> данные (заголовки, таблицы, результаты расчётов)
          в тело отчёта.</li>
          <li>Экспортирует готовый документ в PDF или DOCX.</li>
        </ol>
        <p>Таким образом, один раз настроенный шаблон — например, с логотипом
        вашей организации в шапке — будет применяться во всех подпрограммах.</p>
      </section>

      <section>
        <h3>Где хранятся шаблоны</h3>
        <p>Шаблоны сохраняются локально в вашем браузере (localStorage)
        отдельно для каждого пользователя. Это значит:</p>
        <ul>
          <li>Шаблоны не уходят на сервер и видны только на этом компьютере
          и в этом браузере.</li>
          <li>Если очистить данные сайта в браузере — пользовательские шаблоны
          пропадут, встроенные при следующей загрузке засеются снова.</li>
          <li>Для переноса на другой компьютер или в другой браузер используйте
          <b>Экспорт всех</b> — получите один JSON-файл, — а на новом месте
          нажмите <b>Импорт</b> и выберите его.</li>
        </ul>
      </section>

      <section>
        <h3>Импорт и экспорт</h3>
        <p><b>Экспорт всех</b> — выгружает весь каталог в JSON-файл
        (<code>raschet-report-templates.json</code>). Используйте для бэкапа
        и передачи коллегам.</p>
        <p><b>Экспорт JSON</b> (в карточке одного шаблона) — выгружает только
        этот шаблон.</p>
        <p><b>Импорт…</b> — принимает JSON-файл того же формата. Режим слияния:
        новые шаблоны добавляются, существующие по тому же id не перезаписываются —
        так импорт не затрёт ваши правки.</p>
      </section>

      <section>
        <h3>Подсказки</h3>
        <ul>
          <li>Для пояснительных записок и протоколов используйте шаблон
          <b>«Официальный документ»</b> — у него широкие поля и Times 12 pt
          с выравниванием по ширине, привычным по ГОСТ 2.105.</li>
          <li>Для ведомостей и спецификаций удобен <b>«A4 альбомная»</b> — в него
          влезают широкие таблицы с множеством колонок.</li>
          <li>Для полевых распечаток и карточек — <b>«Краткая справка (A5)»</b>.</li>
          <li>В колонтитулах работают плейсхолдеры:
          <code>{{meta.title}}</code>, <code>{{meta.author}}</code>,
          <code>{{date}}</code>, <code>{{page}}</code>, <code>{{pages}}</code>.</li>
        </ul>
      </section>
    </div>`;
  backdrop.appendChild(modal);
  document.body.appendChild(backdrop);

  const close = () => backdrop.remove();
  modal.querySelector('#help-close').addEventListener('click', close);
  backdrop.addEventListener('click', (e) => { if (e.target === backdrop) close(); });
}
