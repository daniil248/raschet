// ======================================================================
// reports/help.js
// Модальное окно со справкой по использованию страницы «Шаблоны отчётов»
// и canvas-редактора шаблона. Открывается кнопкой «?» в тулбаре каталога.
//
// Содержит объяснения по ключевым моментам:
//   — что такое шаблон и чем он отличается от «содержимого»
//   — как работает canvas-редактор (перетаскивание зон)
//   — какие бывают зоны и для чего они нужны
//   — как подпрограммы применяют шаблон
//   — где хранятся шаблоны и как делать резервные копии
//
// Для разработчиков, встраивающих модуль в свои подпрограммы, есть
// отдельный документ: shared/report/README.md.
// ======================================================================

export function openHelp() {
  const backdrop = document.createElement('div');
  backdrop.className = 'rpt-modal-backdrop';
  const modal = document.createElement('div');
  modal.className = 'rpt-modal';
  modal.style.maxWidth = '820px';
  modal.style.height = 'auto';
  modal.style.maxHeight = '90vh';
  modal.innerHTML = `
    <div class="rpt-modal__hdr">
      <div class="rpt-modal__title">Как пользоваться шаблонами отчётов</div>
      <div>
        <button class="primary" id="help-close">Закрыть</button>
      </div>
    </div>
    <div class="rpt-help-body">

      <section>
        <h3>Что такое шаблон отчёта</h3>
        <p>Шаблон — это <b>оформление</b> документа: формат листа, поля печати,
        расположение логотипа, зоны с заголовком, информацией о компании,
        адресатом, датой, нумерацией и т.д., а также стили заголовков,
        основного текста, списков и таблиц.</p>
        <p>Шаблон <b>не хранит данные расчёта</b>. Данные подставляет подпрограмма
        («Расчёт кабельной линии», «Расчёт АКБ», «Конфигуратор щита», ...) при
        экспорте отчёта — берёт ваш шаблон и вливает в него свои блоки в
        зону отчёта.</p>
      </section>

      <section>
        <h3>Редактор шаблона — как на холсте</h3>
        <p>При нажатии <b>«Редактировать оформление»</b> открывается canvas-редактор.
        Слева — панель настроек и список зон, справа — изображение страницы.
        Зоны располагаются прямо на странице, как блоки на холсте, и могут
        быть перетащены мышью в любое место внутри поля печати.</p>
        <p>На странице всегда видны:</p>
        <ul>
          <li><b>Поле печати</b> — пунктирная рамка, отступ от краёв листа.
          Задаётся на вкладке «Лист» и определяет границы, за которые зоны
          не могут выйти.</li>
          <li><b>Зона отчёта</b> — серая подсказка по центру. Это та область,
          куда подпрограмма вставит своё содержимое (заголовки, таблицы,
          результаты). Её не нужно настраивать — она занимает всё поле печати
          автоматически.</li>
          <li><b>Зоны</b>, которые вы добавили — текстовые блоки и логотип.
          Каждую можно перетащить за середину и растянуть за квадратный
          угол справа-снизу.</li>
        </ul>
      </section>

      <section>
        <h3>Как добавить зону</h3>
        <ol>
          <li>Откройте вкладку <b>«Зоны»</b> на левой панели.</li>
          <li>Нажмите одну из кнопок в секции «Добавить зону»: заголовок
          документа, подзаголовок, информация о компании, адресат, дата,
          номер страницы, автор, свободный текст, логотип.</li>
          <li>Зона появится в центре поля печати. Перетащите её мышью в
          нужное место — она будет ограничена полями печати и не сможет
          выйти за них.</li>
          <li>Кликните по зоне, чтобы открыть вкладку <b>«Свойства»</b> —
          там редактируется её текст, стиль, выравнивание, положение и
          размер с точностью до миллиметра, а также на каких страницах
          зона будет показываться (на всех / только на первой / на всех
          кроме первой).</li>
        </ol>
      </section>

      <section>
        <h3>Клавиатурные сокращения в редакторе</h3>
        <p>Когда фокус не находится в текстовом поле и выделена зона:</p>
        <ul>
          <li><b>Стрелки</b> — сдвиг на 1&nbsp;мм с ограничением по полям печати</li>
          <li><b>Shift + стрелки</b> — сдвиг на 10&nbsp;мм</li>
          <li><b>Delete</b> / <b>Backspace</b> — удалить выделенную зону (или логотип)</li>
          <li><b>Ctrl + D</b> (⌘&nbsp;+&nbsp;D на Mac) — дублировать зону со сдвигом 4&nbsp;мм</li>
          <li><b>Escape</b> — снять выделение (повторное нажатие закрывает редактор)</li>
          <li><b>Ctrl + S</b> (⌘&nbsp;+&nbsp;S) — сохранить и закрыть редактор</li>
        </ul>
        <p>В текстовых полях (Название, Текст зоны, X/Y/Ш/В) все клавиши
        работают стандартно — <b>Escape</b> там только снимает фокус с поля,
        а не закрывает редактор.</p>
      </section>

      <section>
        <h3>Магнитная сетка и перетаскивание</h3>
        <p>При перетаскивании зоны мышью координаты <b>автоматически
        привязываются к сетке 5&nbsp;мм</b> — это даёт аккуратные выровненные
        макеты без ручной подгонки. Если нужно точное позиционирование
        (например, на 47&nbsp;мм, а не на 45 или 50), <b>удерживайте Alt</b>
        во время перетаскивания — снап временно отключается.</p>
        <p>То же работает и при изменении размера зоны за угловую ручку.</p>
        <p>Для сверхточной настройки координат всегда можно открыть
        вкладку <b>«Свойства»</b> и ввести X, Y, ширину и высоту в
        миллиметрах с шагом 0,1&nbsp;мм.</p>
      </section>

      <section>
        <h3>Автоматическая подгонка при смене листа</h3>
        <p>Если вы уже расставили зоны, а потом изменили формат листа
        (например, с A4 на A5), ориентацию или поля печати — все зоны
        <b>автоматически перемещаются внутрь новых границ</b>. Зоны,
        которые оказались бы шире или выше новой печатной области,
        пропорционально уменьшаются.</p>
        <p>Если результат не нравится — поправьте их вручную на холсте
        или во вкладке «Свойства».</p>
      </section>

      <section>
        <h3>Плейсхолдеры в тексте</h3>
        <p>В тексте зоны можно использовать подстановки, которые
        заменяются при формировании отчёта:</p>
        <ul>
          <li><code>{{meta.title}}</code> — заголовок документа (из вкладки
          «Лист» или из метаданных подпрограммы)</li>
          <li><code>{{meta.author}}</code> — автор</li>
          <li><code>{{date}}</code> — текущая дата</li>
          <li><code>{{page}}</code> — номер текущей страницы</li>
          <li><code>{{pages}}</code> — общее число страниц</li>
        </ul>
        <p>Например, для зоны «Номер страницы» можно написать
        <code>стр. {{page}} из {{pages}}</code> — и в финальном документе
        Word или Acrobat подставит реальные номера.</p>
      </section>

      <section>
        <h3>Встроенные и пользовательские шаблоны</h3>
        <p>В каталоге два типа шаблонов:</p>
        <ul>
          <li><b>Встроенные</b> — идут в комплекте (пустой A4, простой отчёт,
          инженерный, расчёт кабеля, АКБ, ИБП, щита, трансформатора,
          официальный документ, техническая записка, ведомость альбомная,
          справка A5). Их нельзя удалить или переименовать, но можно
          <b>клонировать</b> и править клон.</li>
          <li><b>Мои</b> — шаблоны, которые вы создали или клонировали.
          Их можно редактировать, переименовывать, удалять и экспортировать.</li>
        </ul>
        <p>Если вам нужен шаблон похожий на уже существующий — не создавайте
        с нуля, а клонируйте близкий встроенный и подправьте его.</p>
      </section>

      <section>
        <h3>Превью и тестовый PDF</h3>
        <p>Выбранный шаблон показывается справа в виде страниц с
        демо-данными. Для каждого встроенного шаблона демо подобрано под
        его задачу: расчёт кабеля показывает таблицу с сечениями, АКБ —
        параметры разряда, ведомость — спецификацию материалов и т.д.</p>
        <p>Кнопки <b>PDF</b> и <b>DOCX</b> в карточке шаблона формируют
        файл с этим же демо-контентом — чтобы проверить оформление в
        Acrobat или Word до того, как использовать шаблон в реальной
        подпрограмме.</p>
      </section>

      <section>
        <h3>Как подпрограммы используют шаблон</h3>
        <p>Когда вы в подпрограмме (например, «Расчёт кабельной линии»)
        нажимаете «Экспорт отчёта», она:</p>
        <ol>
          <li>Читает список ваших шаблонов и предлагает выбрать подходящий.</li>
          <li>Берёт оформление из шаблона — поля печати, все зоны (логотип,
          шапка, подвал, адресат и т.д.), стили заголовков и таблиц.</li>
          <li>Вставляет <b>свои</b> блоки (заголовки, таблицы, результаты
          расчётов) в зону отчёта. Поток содержимого течёт с переносами
          на следующие страницы, а зоны остаются на своих местах.</li>
          <li>Экспортирует готовый документ в PDF или DOCX.</li>
        </ol>
        <p>Таким образом, один раз настроенный шаблон — например, с логотипом
        и реквизитами вашей организации в шапке — применяется во всех
        подпрограммах единообразно.</p>
      </section>

      <section>
        <h3>Где хранятся шаблоны</h3>
        <p>Шаблоны сохраняются локально в вашем браузере (localStorage)
        отдельно для каждого пользователя. Это значит:</p>
        <ul>
          <li>Шаблоны не уходят на сервер и видны только на этом
          компьютере и в этом браузере.</li>
          <li>Если очистить данные сайта — пользовательские шаблоны
          пропадут, встроенные при следующей загрузке засеются заново.</li>
          <li>Для переноса на другой компьютер используйте
          <b>«Экспорт всех»</b> — получите один JSON-файл, — а на новом
          месте нажмите <b>«Импорт»</b>.</li>
        </ul>
      </section>

      <section>
        <h3>Ограничения DOCX-экспорта</h3>
        <p>PDF-экспорт сохраняет положение всех зон с точностью до
        миллиметра. DOCX-формат не поддерживает свободно позиционируемый
        текст так же точно: зоны, расположенные в верхней половине страницы,
        переносятся в колонтитул шапки Word, нижние — в колонтитул подвала.
        Точные координаты при этом теряются — но общий макет и стили
        сохраняются.</p>
        <p>Если нужен побайтовый контроль оформления — выбирайте PDF.
        DOCX удобен, когда отчёт надо дорабатывать вручную.</p>
      </section>

      <section>
        <h3>Подсказки</h3>
        <ul>
          <li>Для пояснительных записок — <b>«Официальный документ»</b>.
          Широкие поля, Times 12 pt, выравнивание по ширине.</li>
          <li>Для спецификаций — <b>«Ведомость (A4 альбомная)»</b>. В неё
          входят широкие таблицы.</li>
          <li>Для полевых распечаток — <b>«Краткая справка (A5)»</b>.</li>
          <li>Поля печати задаются на вкладке «Лист» — именно они ограничивают
          область, куда можно поместить зоны.</li>
          <li>Если зону случайно поставили не туда — откройте её «Свойства»
          и введите точные координаты X и Y в миллиметрах.</li>
        </ul>
      </section>

    </div>`;
  backdrop.appendChild(modal);
  document.body.appendChild(backdrop);

  const close = () => backdrop.remove();
  modal.querySelector('#help-close').addEventListener('click', close);
  backdrop.addEventListener('click', (e) => { if (e.target === backdrop) close(); });
}
